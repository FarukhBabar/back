export const Login = async (req , res ) =>{
    const{email , password} = req.body
    try {
        const user = await userModel.findOne({email})
        if(!user){
            return res.status(404).send({Message:"Email not Exist"})
        }
        const ismatch = await bcrypt.compare(password,user.password)
        if(!ismatch){
            return res.status(400).send({Message: "Invalid Credentials"})
        }
        const token  = Jwt.sign({id:user._id , role:user.role} , process.env.JWT_KEY , {expiresIn:"10m"} )
        return res.status(200).send({token , name:user.name ,role:user.role ,picture: user.image})
    } catch (error) {
        console.log(error)
    }
}
